                                <p>Il s’agit d’un stage destiné aux jeunes conducteurs. En effet, un conducteur qui
                                    s’est vu retirer au moins 3 points suite à une infraction alors qu’il se trouve en
                                    période probatoire doit obligatoirement participer à un stage.</p>

                                <p>Le Ministère de l’Intérieur envoie alors la lettre 48N au conducteur. Celui-ci a
